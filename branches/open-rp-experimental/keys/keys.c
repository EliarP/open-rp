#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>

#include "config.h"

static uint8_t skey0[ORP_KEY_LEN] = {
	0xD1, 0xB2, 0x12, 0xEB, 0x73, 0x86, 0x6C, 0x7B,
	0x12, 0xA7, 0x5E, 0x0C, 0x04, 0xC6, 0xB8, 0x91
};
static uint8_t skey1[ORP_KEY_LEN] = {
	0x1F, 0xD5, 0xB9, 0xFA, 0x71, 0xB8, 0x96, 0x81,
	0xB2, 0x87, 0x92, 0xE2, 0x6F, 0x38, 0xC3, 0x6F
};
static uint8_t skey2[ORP_KEY_LEN] = {
	0x65, 0x2D, 0x8C, 0x90, 0xDE, 0x87, 0x17, 0xCF,
	0x4F, 0xB3, 0xD8, 0xD3, 0x01, 0x79, 0x6B, 0x59
};

int main(int argc, char *argv[])
{
	struct orpConfigCtx_t config_ctx;
	if (orpConfigOpen(&config_ctx, "keys.orp") < 0) return 1;
	config_ctx.header.flags = ORP_CONFIG_KEYS;
	orpConfigSetKey(&config_ctx, orpID_KEY_0, skey0);
	orpConfigSetKey(&config_ctx, orpID_KEY_1, skey1);
	orpConfigSetKey(&config_ctx, orpID_KEY_2, skey2);
	orpConfigClose(&config_ctx);
	return 0;
}

